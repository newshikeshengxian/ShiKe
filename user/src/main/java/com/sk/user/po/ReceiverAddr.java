package com.sk.user.po;


public class ReceiverAddr {

  private String raId;
  private String userId;
  private String raName;
  private String raPhone;
  private String raAddr;
  private String raTel;
  private String zipCode;
  private Long raState;
  private String detailaddr;
  private String defaultAddr;

  public Long getRaState() {
    return raState;
  }

  public void setRaState(Long raState) {
    this.raState = raState;
  }

  public String getDetailaddr() {
    return detailaddr;
  }

  public void setDetailaddr(String detailaddr) {
    this.detailaddr = detailaddr;
  }

  public String getDefaultAddr() {
    return defaultAddr;
  }

  public void setDefaultAddr(String defaultAddr) {
    this.defaultAddr = defaultAddr;
  }

  public String getRaId() {
    return raId;
  }

  public void setRaId(String raId) {
    this.raId = raId;
  }


  public String getUserId() {
    return userId;
  }

  public void setUserId(String userId) {
    this.userId = userId;
  }


  public String getRaName() {
    return raName;
  }

  public void setRaName(String raName) {
    this.raName = raName;
  }


  public String getRaPhone() {
    return raPhone;
  }

  public void setRaPhone(String raPhone) {
    this.raPhone = raPhone;
  }


  public String getRaAddr() {
    return raAddr;
  }

  public void setRaAddr(String raAddr) {
    this.raAddr = raAddr;
  }


  public String getRaTel() {
    return raTel;
  }

  public void setRaTel(String raTel) {
    this.raTel = raTel;
  }


  public String getZipCode() {
    return zipCode;
  }

  public void setZipCode(String zipCode) {
    this.zipCode = zipCode;
  }

}
